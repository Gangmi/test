package b_operator;

public class Ex07_BinaryLogical {

	public static void main(String[] args) {
		
		
		/* 
		 * 이진논리 : &(and) | (or) , ^(xor) exclusive or 이라고 함, 
		 *  -> 이진수로 바꾸고 각 연산자에 따라서 계산한다.
		 *  
		 */
		int a =15;
		int b =10;
		System.out.println(a&b); // 0
		System.out.println(a|b);
		System.out.println(a^b);
		
		


	}

}
